export async function migrateJobsToFirebase() {
  try {
    const projectsCollection = collection(db, "translations");

    // Usando Promise.all para adicionar todos os documentos de uma vez
    const results = await Promise.all(
      projectsData.map(async (project) => {
        // Removendo o id do Supabase para o Firestore gerar seu próprio ID
        const { id, ...projectData } = project;

        // Adicionando o documento à coleção 'projects'
        const docRef = await addDoc(projectsCollection, {
          ...projectData,
          // Convertendo a string de data para Timestamp do Firestore
          created_at: new Date(project.created_at),
          // Mantendo o order como número
        });

        return { originalId: id, firebaseId: docRef.id };
      }),
    );

    console.log("Migração concluída com sucesso!");
    console.log("Resultados:", results);
    return results;
  } catch (error) {
    console.error("Erro durante a migração:", error);
    throw error;
  }
}
